<br>Our History</center>

<br>The Delaware SPCA is a private non-profit organization dedicated to the prevention of cruelty and the encouragement of humane treatment of all animals. 

<br>Established in 1873, Delaware SPCA is the oldest animal welfare organization in the state and has provided continuous service to pets and their companions for more than 135 years.

<br>The society was incorporated in 1873 in response to the mistreatment of livestock being led to market in the city of Wilmington.

<br>The late 1890's saw the society begin its contract with the city of Wilmington for dog control, establishing a relationship that has continued to the present day. <br>The same period also saw the Delaware SPCA begin its work of encouraging humane education in schools.

<br>The Delaware SPCA was based in Wilmington until 1963, when operations were expanded and a new shelter was constructed in Newark. <br>The society moved again in 1973 to the current location of the Stanton shelter when the previous property was annexed for an expansion of the New Castle County Airport.

<br>In the early 1970's, the Delaware SPCA Sussex Chapter was formally annexed, and the Georgetown shelter was opened on August 1, 1972.<br> In 2004 the Georgetown shelter was renovated and equipped with a neutering clinic, allowing the Sussex Chapter to offer low cost spay/neuter services to area residents.

<br>In 2009, the society opened the Jane R. Haggard Spay/Neuter Clinic located at the organization's headquarters in Stanton. The clinic is the first of it's kind in Delaware, providing high-volume, high-quality (HVHQ), low-cost spay/neuter services to the public and rescue groups.

<br>In early 2010, in keeping with the commitment to end pet overpopulation, a new spay/neuter van was acquired, and provides transportation of animals across the state to the clinic, <br>removing access as a barrier to services.

<br>In 2011, a second HVHQ spay/neuter clinic was established at our Georgetown clinic to expand our services to all of Delaware. In 2011, we neutered 7,200 animals, <br>more than any other provider in Delaware.

The work that began in the late 19th century continues today in the early 21st as the Delaware SPCA continues in its work of promoting the protection and the humane treatment of our animal friends, four-legged and otherwise.

This work is only possible through the support of the community and concerned citizens like you.




